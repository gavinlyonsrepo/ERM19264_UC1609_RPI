#include <bcm2835.h>
#include "ERM19264_UC1609.h"
#include <time.h>
#include <stdio.h>

#define LCDcontrast 0x49 //Constrast 00 to FF , 0x49 is default. user adjust
#define myLCDwidth  192
#define myLCDheight 64


// GPIO 
#define RST 25 // GPIO pin number pick any you want
#define CD 24 // GPIO pin number pick any you want 

ERM19264_UC1609 myLCD(myLCDwidth ,myLCDheight, RST, CD) ; // instantiate  an object 

// =============== Function prototype ================
void setup(void);
void myLoop(void);
void Test1(void);
void Test2(void);
void Test3(void);

// ======================= Main ===================
int main(int argc, char **argv) 
{
	if(!bcm2835_init())
	{
	    return -1;
	}
	printf("LCD Begin\r\n");
	bcm2835_delay(500);
	setup();
	myLoop();
	myLCD.LCDSPIoff();
	myLCD.LCDPowerDown();
	bcm2835_close(); // Close library & /dev/mem, deallocating any allocated memory 
	printf("LCD End\r\n");
	return 0;
}

