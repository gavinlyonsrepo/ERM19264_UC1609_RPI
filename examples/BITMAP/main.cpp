void setup()
{
  myLCD.LCDbegin(LCDcontrast); // initialize the LCD
  myLCD.LCDFillScreen(0xFF, 0); // Clears screen
  bcm2835_delay(5000);
}

void myLoop()
{
	Test1(); // Method (1) LCD bitmap method, Any buffer setting on.
	Test2(); // Method (2) LCD buffer method, MULTI_BUFFER or SINGLE BUFFER setting on.
	Test3(); // Method (3) LCD update mutlibuffer mode, MULTI_BUFFER setting on.
}


void Test1(void)
{
	// Method (1) LCD bitmap method, Any buffer  setting on 
	//  x ,y,w,h, bitmap
	myLCD.LCDBitmap(0, 0 , myLCDwidth, myLCDheight, fullscreenBitmap);
	bcm2835_delay(5000);
	myLCD.LCDFillScreen(0x00, 0); 
}

void Test2(void)
{
	// Method (2) LCD buffer method , MULTI_BUFFER or SINGLE BUFFER setting, 
	//  x ,y,w,h, bitmap
	myLCD.LCDBuffer(30, 10, 20, 20, (uint8_t*)smallImage);
	myLCD.LCDBuffer(70, 10, 20, 20, (uint8_t*)smallImage);
	bcm2835_delay(5000);
	myLCD.LCDFillScreen(0x00, 0); 
}

void Test3(void)
{
	// Method (3) multibuffer mode, MULTI_BUFFER setting on,
	MultiBuffer Whole_screen;
	Whole_screen.screenbitmap = (uint8_t*) fullscreenBitmap;
	Whole_screen.width = myLCDwidth;
	Whole_screen.height = myLCDheight;
	Whole_screen.xoffset = 0; // offset on display
	Whole_screen.yoffset = 0 ;
	myLCD.ActiveBuffer = &Whole_screen;
	myLCD.LCDupdate();
	bcm2835_delay(5000);
	myLCD.LCDFillScreen(0x00, 0); 
}
